def area(v):
	a=(v[1][0] * v[2][1])
	b=(v[2][0] * v[1][1])
	c=(v[0][0] * v[2][1])
	d=(v[2][0] * v[0][1])
	e=(v[0][0] * v[1][1]) 
	f=(v[1][0] * v[0][1])
	result =(1.0*(a-b-c+d+e-f))/2.0
	print ("The area of the triangle is "+ str(result))

 
 
vertices = [[0,0],[1,0],[0,2]]
area(vertices)

 
